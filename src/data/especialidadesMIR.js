// Lookup: código corto CTO → nombre de especialidad
// Ej: codigoCTO['CD'] === 'Cardiología'
export const codigoCTO = Object.fromEntries(
  Object.entries(especialidadesMIR)
    .filter(([, v]) => v.codigo)
    .map(([nombre, v]) => [v.codigo.split('.').pop(), nombre])
)

// Lookup inverso: nombre → código corto
// Ej: codigoCTOInverso['Cardiología'] === 'CD'
export const codigoCTOInverso = Object.fromEntries(
  Object.entries(codigoCTO).map(([cod, nombre]) => [nombre, cod])
)

